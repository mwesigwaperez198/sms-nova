def normalize_theme_key(theme_key: str | None) -> str:
    return theme_key if theme_key in THEMES else DEFAULT_THEME_KEY


def theme_options() -> list[tuple[str, str]]:
    return [(key, data["name"]) for key, data in THEMES.items()]


def theme_palette(theme_key: str | None = None) -> dict[str, str]:
    return THEMES[normalize_theme_key(theme_key)]


