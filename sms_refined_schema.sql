    CONSTRAINT fk_teacher_assignments_stream_school
        FOREIGN KEY (school_id, stream_id)
        REFERENCES streams(school_id, id),
    CONSTRAINT fk_teacher_assignments_subject_school
        FOREIGN KEY (school_id, subject_id)
        REFERENCES subjects(school_id, id),
    CONSTRAINT fk_teacher_assignments_term_school
        FOREIGN KEY (school_id, term_id)
        REFERENCES terms(school_id, id)
);

CREATE INDEX ix_teacher_assignments_school_id ON teacher_assignments (school_id);

CREATE TABLE student_enrollments (
    id                  INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    school_id           INTEGER NOT NULL REFERENCES schools(id) ON DELETE CASCADE,
    student_id          INTEGER NOT NULL,
    class_id            INTEGER NOT NULL,
    stream_id           INTEGER,
    academic_year_id    INTEGER NOT NULL,
    enrolled_at         DATE NOT NULL DEFAULT CURRENT_DATE,
    exited_at           DATE,
    created_at          TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at          TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    CONSTRAINT uq_student_enrollments_school_student_year
        UNIQUE (school_id, student_id, academic_year_id),
    CONSTRAINT fk_student_enrollments_student_school
        FOREIGN KEY (school_id, student_id)
        REFERENCES students(school_id, id)
        ON DELETE CASCADE,
    CONSTRAINT fk_student_enrollments_class_school
        FOREIGN KEY (school_id, class_id)
        REFERENCES classes(school_id, id),
    CONSTRAINT fk_student_enrollments_stream_school
        FOREIGN KEY (school_id, stream_id)
        REFERENCES streams(school_id, id),
