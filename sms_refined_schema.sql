-- ============================================================
-- SMART SCHOOL MANAGEMENT SYSTEM - REFINED POSTGRESQL SCHEMA
-- Reference only. This file has NOT been applied to the app.
--
-- Design goals:
-- - Match the current smart_school_backend naming where practical.
-- - Keep integer role/user/school/student IDs compatible with current code.
-- - Add production-ready tenant structure, academic setup, fees,
--   attendance, reports, notifications, sync, and audit support.
-- - Use VARCHAR + CHECK constraints instead of PostgreSQL ENUMs so
--   future migrations remain easier.
--
-- Do not run this against the existing database directly. Convert it
-- into Alembic migrations module by module when implementation starts.
-- ============================================================

CREATE EXTENSION IF NOT EXISTS pgcrypto;

-- ============================================================
-- Helper: updated_at trigger
-- ============================================================

CREATE OR REPLACE FUNCTION set_updated_at()
RETURNS TRIGGER AS $$
BEGIN
    NEW.updated_at = NOW();
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

-- ============================================================
-- 1. RBAC
-- ============================================================

CREATE TABLE roles (
    id              INTEGER PRIMARY KEY,
    name            VARCHAR(50) NOT NULL UNIQUE,
    description     VARCHAR(255),
    is_system_role  BOOLEAN NOT NULL DEFAULT TRUE,
    created_at      TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE TABLE permissions (
    id              INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    code            VARCHAR(100) NOT NULL UNIQUE,
    description     VARCHAR(255),
    created_at      TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE TABLE role_permissions (
    role_id         INTEGER NOT NULL REFERENCES roles(id) ON DELETE CASCADE,
    permission_id   INTEGER NOT NULL REFERENCES permissions(id) ON DELETE CASCADE,
    PRIMARY KEY (role_id, permission_id)
);

-- ============================================================
-- 2. Platform / Tenants
-- ============================================================

CREATE TABLE schools (
    id                    INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name                  VARCHAR(150) NOT NULL,
    school_code           VARCHAR(30) NOT NULL UNIQUE,
    address               TEXT,
    phone                 VARCHAR(50),
    email                 VARCHAR(255),
    logo_url              TEXT,
    country               VARCHAR(80) NOT NULL DEFAULT 'Uganda',
    currency_code         VARCHAR(3) NOT NULL DEFAULT 'UGX',
    timezone              VARCHAR(80) NOT NULL DEFAULT 'Africa/Kampala',
    subscription_status   VARCHAR(30) NOT NULL DEFAULT 'trial',
    is_active             BOOLEAN NOT NULL DEFAULT TRUE,
    created_at            TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at            TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    CONSTRAINT ck_schools_subscription_status
        CHECK (subscription_status IN ('trial', 'active', 'expired', 'cancelled', 'suspended')),
    CONSTRAINT ck_schools_currency_code
        CHECK (currency_code = UPPER(currency_code) AND length(currency_code) = 3)
);

CREATE UNIQUE INDEX uq_schools_code_ci ON schools (lower(school_code));
CREATE INDEX ix_schools_active ON schools (is_active);

CREATE TABLE school_settings (
    id                      INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    school_id               INTEGER NOT NULL UNIQUE REFERENCES schools(id) ON DELETE CASCADE,
    terms_per_year          INTEGER NOT NULL DEFAULT 3,
    grading_system          VARCHAR(50) NOT NULL DEFAULT 'UNEB',
    sms_notifications       BOOLEAN NOT NULL DEFAULT TRUE,
    email_notifications     BOOLEAN NOT NULL DEFAULT TRUE,
    allow_partial_payment   BOOLEAN NOT NULL DEFAULT TRUE,
    low_bandwidth_mode      BOOLEAN NOT NULL DEFAULT TRUE,
    created_at              TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at              TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    CONSTRAINT ck_school_settings_terms
        CHECK (terms_per_year BETWEEN 1 AND 4)
);

CREATE TABLE school_branding (
    id                INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    school_id         INTEGER NOT NULL UNIQUE REFERENCES schools(id) ON DELETE CASCADE,
    primary_color     VARCHAR(20),
    secondary_color   VARCHAR(20),
    banner_url        TEXT,
    motto             TEXT,
    created_at        TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at        TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE TABLE subscription_plans (
    id              INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name            VARCHAR(100) NOT NULL UNIQUE,
    price           NUMERIC(12, 2) NOT NULL,
    currency_code   VARCHAR(3) NOT NULL DEFAULT 'UGX',
    max_students    INTEGER,
    max_teachers    INTEGER,
    features        JSONB NOT NULL DEFAULT '{}'::jsonb,
    is_active       BOOLEAN NOT NULL DEFAULT TRUE,
    created_at      TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    CONSTRAINT ck_subscription_plans_price
        CHECK (price >= 0),
    CONSTRAINT ck_subscription_plans_currency
        CHECK (currency_code = UPPER(currency_code) AND length(currency_code) = 3),
    CONSTRAINT ck_subscription_plans_limits
        CHECK (
            (max_students IS NULL OR max_students > 0)
            AND (max_teachers IS NULL OR max_teachers > 0)
        )
);

CREATE TABLE school_subscriptions (
    id              INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    school_id       INTEGER NOT NULL REFERENCES schools(id) ON DELETE CASCADE,
    plan_id         INTEGER NOT NULL REFERENCES subscription_plans(id),
    status          VARCHAR(30) NOT NULL DEFAULT 'trial',
    starts_at       DATE NOT NULL,
    expires_at      DATE NOT NULL,
    created_at      TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at      TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    CONSTRAINT ck_school_subscriptions_status
        CHECK (status IN ('trial', 'active', 'expired', 'cancelled', 'suspended')),
    CONSTRAINT ck_school_subscriptions_dates
        CHECK (expires_at >= starts_at)
);

CREATE INDEX ix_school_subscriptions_school ON school_subscriptions (school_id);
CREATE INDEX ix_school_subscriptions_status ON school_subscriptions (status);

-- ============================================================
-- 3. Users and Authentication
-- ============================================================

CREATE TABLE users (
    id              INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name            VARCHAR(150) NOT NULL,
    email           VARCHAR(255) NOT NULL,
    password_hash   VARCHAR(255) NOT NULL,
    role_id         INTEGER NOT NULL REFERENCES roles(id),
    school_id       INTEGER REFERENCES schools(id),
    phone           VARCHAR(50),
    is_active       BOOLEAN NOT NULL DEFAULT TRUE,
    last_login_at   TIMESTAMPTZ,
    created_at      TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at      TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    CONSTRAINT ck_users_school_scope_by_role
        CHECK (
            (role_id = 1 AND school_id IS NULL)
            OR
            (role_id <> 1 AND school_id IS NOT NULL)
        )
);

CREATE UNIQUE INDEX uq_users_email_ci ON users (lower(email));
CREATE INDEX ix_users_school_id ON users (school_id);
CREATE INDEX ix_users_role_id ON users (role_id);
CREATE INDEX ix_users_active ON users (is_active);
CREATE UNIQUE INDEX uq_users_school_id_pair ON users (school_id, id);

CREATE TABLE refresh_tokens (
    id              INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id         INTEGER NOT NULL REFERENCES users(id) ON DELETE CASCADE,
    token_hash      TEXT NOT NULL UNIQUE,
    device_name     VARCHAR(100),
    ip_address      VARCHAR(80),
    user_agent      TEXT,
    expires_at      TIMESTAMPTZ NOT NULL,
    revoked_at      TIMESTAMPTZ,
    created_at      TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE INDEX ix_refresh_tokens_user_id ON refresh_tokens (user_id);
CREATE INDEX ix_refresh_tokens_expires_at ON refresh_tokens (expires_at);

CREATE TABLE login_attempts (
    id              INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id         INTEGER REFERENCES users(id) ON DELETE SET NULL,
    email           VARCHAR(255),
    ip_address      VARCHAR(80),
    success         BOOLEAN NOT NULL,
    failure_reason  VARCHAR(120),
    attempted_at    TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE INDEX ix_login_attempts_user_id ON login_attempts (user_id);
CREATE INDEX ix_login_attempts_email ON login_attempts (lower(email));
CREATE INDEX ix_login_attempts_attempted_at ON login_attempts (attempted_at);

-- ============================================================
-- 4. People
-- ============================================================

CREATE TABLE students (
    id                  INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    school_id           INTEGER NOT NULL REFERENCES schools(id),
    user_id             INTEGER UNIQUE REFERENCES users(id) ON DELETE SET NULL,
    admission_number    VARCHAR(80),
    name                VARCHAR(150) NOT NULL,
    class_name          VARCHAR(80),
    stream_name         VARCHAR(80),
    date_of_birth       DATE,
    gender              VARCHAR(20),
    photo_url           TEXT,
    is_active           BOOLEAN NOT NULL DEFAULT TRUE,
    created_at          TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at          TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    CONSTRAINT uq_students_school_admission
        UNIQUE (school_id, admission_number),
    CONSTRAINT ck_students_gender
        CHECK (gender IS NULL OR gender IN ('male', 'female', 'other')),
    CONSTRAINT ck_students_birth_date
        CHECK (date_of_birth IS NULL OR date_of_birth <= CURRENT_DATE)
);

CREATE INDEX ix_students_school_id ON students (school_id);
CREATE INDEX ix_students_admission_number ON students (admission_number);
CREATE INDEX ix_students_class_stream ON students (school_id, class_name, stream_name);
CREATE UNIQUE INDEX uq_students_school_id_pair ON students (school_id, id);

CREATE TABLE student_guardians (
    id              INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    school_id       INTEGER NOT NULL REFERENCES schools(id) ON DELETE CASCADE,
    student_id      INTEGER NOT NULL,
    guardian_id     INTEGER NOT NULL,
    relationship    VARCHAR(50),
    is_primary      BOOLEAN NOT NULL DEFAULT FALSE,
    can_receive_sms BOOLEAN NOT NULL DEFAULT TRUE,
    can_make_payment BOOLEAN NOT NULL DEFAULT TRUE,
    created_at      TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    CONSTRAINT uq_student_guardian
        UNIQUE (student_id, guardian_id),
    CONSTRAINT fk_student_guardians_student_school
        FOREIGN KEY (school_id, student_id)
        REFERENCES students(school_id, id)
        ON DELETE CASCADE,
    CONSTRAINT fk_student_guardians_guardian_school
        FOREIGN KEY (school_id, guardian_id)
        REFERENCES users(school_id, id)
        ON DELETE CASCADE
);

CREATE INDEX ix_student_guardians_student_id ON student_guardians (student_id);
CREATE INDEX ix_student_guardians_guardian_id ON student_guardians (guardian_id);
CREATE UNIQUE INDEX uq_student_guardians_primary
    ON student_guardians (student_id)
    WHERE is_primary IS TRUE;

CREATE TABLE staff_profiles (
    id              INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    school_id       INTEGER NOT NULL REFERENCES schools(id) ON DELETE CASCADE,
    user_id         INTEGER NOT NULL,
    staff_number    VARCHAR(80),
    department      VARCHAR(100),
    date_joined     DATE,
    created_at      TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at      TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    CONSTRAINT uq_staff_profiles_school_staff_number
        UNIQUE (school_id, staff_number),
    CONSTRAINT uq_staff_profiles_user
        UNIQUE (user_id),
    CONSTRAINT fk_staff_profiles_user_school
        FOREIGN KEY (school_id, user_id)
        REFERENCES users(school_id, id)
        ON DELETE CASCADE
);

CREATE TABLE teacher_profiles (
    id                  INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    school_id           INTEGER NOT NULL REFERENCES schools(id) ON DELETE CASCADE,
    user_id             INTEGER NOT NULL,
    employee_number     VARCHAR(80),
    qualification       TEXT,
    specialization      VARCHAR(100),
    date_employed       DATE,
    created_at          TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at          TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    CONSTRAINT uq_teacher_profiles_school_employee
        UNIQUE (school_id, employee_number),
    CONSTRAINT uq_teacher_profiles_user
        UNIQUE (user_id),
    CONSTRAINT fk_teacher_profiles_user_school
        FOREIGN KEY (school_id, user_id)
