CREATE INDEX ix_student_enrollments_student_id ON student_enrollments (student_id);
CREATE INDEX ix_student_enrollments_school_id ON student_enrollments (school_id);

-- ============================================================
-- 6. Fees and Payments
-- ============================================================

CREATE TABLE fee_categories (
    id              INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    school_id       INTEGER NOT NULL REFERENCES schools(id) ON DELETE CASCADE,
    name            VARCHAR(80) NOT NULL,
    description     TEXT,
    is_active       BOOLEAN NOT NULL DEFAULT TRUE,
    created_at      TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at      TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    CONSTRAINT uq_fee_categories_school_name
        UNIQUE (school_id, name)
);

CREATE INDEX ix_fee_categories_school_id ON fee_categories (school_id);
CREATE UNIQUE INDEX uq_fee_categories_school_id_pair ON fee_categories (school_id, id);

CREATE TABLE fee_structures (
    id                  INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    school_id           INTEGER NOT NULL REFERENCES schools(id) ON DELETE CASCADE,
    fee_category_id     INTEGER NOT NULL,
    class_id            INTEGER,
    term_id             INTEGER,
    amount              NUMERIC(12, 2) NOT NULL,
    description         TEXT,
    is_active           BOOLEAN NOT NULL DEFAULT TRUE,
    created_at          TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at          TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    CONSTRAINT fk_fee_structures_category_school
        FOREIGN KEY (school_id, fee_category_id)
        REFERENCES fee_categories(school_id, id),
    CONSTRAINT fk_fee_structures_class_school
        FOREIGN KEY (school_id, class_id)
        REFERENCES classes(school_id, id),
    CONSTRAINT fk_fee_structures_term_school
        FOREIGN KEY (school_id, term_id)
        REFERENCES terms(school_id, id),
    CONSTRAINT ck_fee_structures_amount
        CHECK (amount >= 0)
);

CREATE INDEX ix_fee_structures_school_id ON fee_structures (school_id);

CREATE TABLE invoices (
    id                  INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    student_id          INTEGER NOT NULL,
    school_id           INTEGER NOT NULL REFERENCES schools(id) ON DELETE CASCADE,
    fee_category_id     INTEGER,
    academic_year       VARCHAR(20) NOT NULL,
    term                VARCHAR(20) NOT NULL,
    academic_year_id    INTEGER,
    term_id             INTEGER,
    amount              NUMERIC(12, 2) NOT NULL,
    description         TEXT,
    due_date            DATE NOT NULL,
    status              VARCHAR(20) NOT NULL DEFAULT 'unpaid',
    created_by_id       INTEGER,
    created_at          TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at          TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    CONSTRAINT fk_invoices_student_school
        FOREIGN KEY (school_id, student_id)
        REFERENCES students(school_id, id),
    CONSTRAINT fk_invoices_category_school
        FOREIGN KEY (school_id, fee_category_id)
        REFERENCES fee_categories(school_id, id),
    CONSTRAINT fk_invoices_academic_year_school
        FOREIGN KEY (school_id, academic_year_id)
        REFERENCES academic_years(school_id, id),
    CONSTRAINT fk_invoices_term_school
        FOREIGN KEY (school_id, term_id)
        REFERENCES terms(school_id, id),
    CONSTRAINT fk_invoices_created_by_school
        FOREIGN KEY (school_id, created_by_id)
        REFERENCES users(school_id, id),
    CONSTRAINT ck_invoices_amount
        CHECK (amount >= 0),
    CONSTRAINT ck_invoices_status
        CHECK (status IN ('unpaid', 'partial', 'paid', 'overdue', 'cancelled', 'void'))
);

CREATE INDEX ix_invoices_school_id ON invoices (school_id);
CREATE INDEX ix_invoices_student_id ON invoices (student_id);
CREATE INDEX ix_invoices_status ON invoices (status);
CREATE INDEX ix_invoices_period ON invoices (school_id, academic_year, term);
CREATE UNIQUE INDEX uq_invoices_school_id_pair ON invoices (school_id, id);

CREATE TABLE invoice_items (
    id                  INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    school_id           INTEGER NOT NULL REFERENCES schools(id) ON DELETE CASCADE,
    invoice_id          INTEGER NOT NULL,
    fee_category_id     INTEGER,
    description         VARCHAR(255) NOT NULL,
    amount              NUMERIC(12, 2) NOT NULL,
    created_at          TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    CONSTRAINT fk_invoice_items_invoice_school
