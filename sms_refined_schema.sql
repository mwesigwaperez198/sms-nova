CREATE TABLE parent_profiles (
    id              INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    school_id       INTEGER NOT NULL REFERENCES schools(id) ON DELETE CASCADE,
    user_id         INTEGER NOT NULL,
    occupation      VARCHAR(100),
    address         TEXT,
    created_at      TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at      TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    CONSTRAINT uq_parent_profiles_user
        UNIQUE (user_id),
    CONSTRAINT fk_parent_profiles_user_school
        FOREIGN KEY (school_id, user_id)
        REFERENCES users(school_id, id)
        ON DELETE CASCADE
);

-- ============================================================
-- 5. Academic Structure
-- ============================================================

CREATE TABLE academic_years (
    id              INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    school_id       INTEGER NOT NULL REFERENCES schools(id) ON DELETE CASCADE,
    name            VARCHAR(20) NOT NULL,
    start_date      DATE NOT NULL,
    end_date        DATE NOT NULL,
    is_current      BOOLEAN NOT NULL DEFAULT FALSE,
    created_at      TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at      TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    CONSTRAINT uq_academic_years_school_name
        UNIQUE (school_id, name),
    CONSTRAINT ck_academic_years_dates
        CHECK (end_date >= start_date)
);

CREATE INDEX ix_academic_years_school_id ON academic_years (school_id);
CREATE UNIQUE INDEX uq_academic_years_one_current
    ON academic_years (school_id)
    WHERE is_current IS TRUE;
CREATE UNIQUE INDEX uq_academic_years_school_id_pair ON academic_years (school_id, id);

CREATE TABLE terms (
    id                  INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    school_id           INTEGER NOT NULL REFERENCES schools(id) ON DELETE CASCADE,
    academic_year_id    INTEGER NOT NULL,
    name                VARCHAR(50) NOT NULL,
    start_date          DATE NOT NULL,
    end_date            DATE NOT NULL,
    is_current          BOOLEAN NOT NULL DEFAULT FALSE,
    created_at          TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at          TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    CONSTRAINT uq_terms_school_year_name
        UNIQUE (school_id, academic_year_id, name),
    CONSTRAINT fk_terms_academic_year_school
        FOREIGN KEY (school_id, academic_year_id)
        REFERENCES academic_years(school_id, id)
        ON DELETE CASCADE,
    CONSTRAINT ck_terms_dates
        CHECK (end_date >= start_date)
);

CREATE INDEX ix_terms_school_id ON terms (school_id);
CREATE UNIQUE INDEX uq_terms_one_current
    ON terms (school_id)
    WHERE is_current IS TRUE;
CREATE UNIQUE INDEX uq_terms_school_id_pair ON terms (school_id, id);

CREATE TABLE classes (
    id              INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    school_id       INTEGER NOT NULL REFERENCES schools(id) ON DELETE CASCADE,
    name            VARCHAR(80) NOT NULL,
    level           INTEGER,
    is_active       BOOLEAN NOT NULL DEFAULT TRUE,
    created_at      TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at      TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    CONSTRAINT uq_classes_school_name
        UNIQUE (school_id, name)
);

CREATE INDEX ix_classes_school_id ON classes (school_id);
CREATE UNIQUE INDEX uq_classes_school_id_pair ON classes (school_id, id);

CREATE TABLE streams (
    id              INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    school_id       INTEGER NOT NULL REFERENCES schools(id) ON DELETE CASCADE,
    class_id        INTEGER NOT NULL,
    name            VARCHAR(80) NOT NULL,
    is_active       BOOLEAN NOT NULL DEFAULT TRUE,
    created_at      TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at      TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    CONSTRAINT uq_streams_school_class_name
        UNIQUE (school_id, class_id, name),
    CONSTRAINT fk_streams_class_school
        FOREIGN KEY (school_id, class_id)
        REFERENCES classes(school_id, id)
        ON DELETE CASCADE
);

CREATE INDEX ix_streams_class_id ON streams (class_id);
CREATE UNIQUE INDEX uq_streams_school_id_pair ON streams (school_id, id);

CREATE TABLE subjects (
    id              INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    school_id       INTEGER NOT NULL REFERENCES schools(id) ON DELETE CASCADE,
    name            VARCHAR(100) NOT NULL,
    code            VARCHAR(30),
    is_active       BOOLEAN NOT NULL DEFAULT TRUE,
    created_at      TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at      TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    CONSTRAINT uq_subjects_school_name
        UNIQUE (school_id, name),
    CONSTRAINT uq_subjects_school_code
        UNIQUE (school_id, code)
);

CREATE INDEX ix_subjects_school_id ON subjects (school_id);
CREATE UNIQUE INDEX uq_subjects_school_id_pair ON subjects (school_id, id);

CREATE TABLE class_subjects (
    id              INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    school_id       INTEGER NOT NULL REFERENCES schools(id) ON DELETE CASCADE,
    class_id        INTEGER NOT NULL,
    subject_id      INTEGER NOT NULL,
    created_at      TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    CONSTRAINT uq_class_subjects_school_class_subject
        UNIQUE (school_id, class_id, subject_id),
    CONSTRAINT fk_class_subjects_class_school
        FOREIGN KEY (school_id, class_id)
        REFERENCES classes(school_id, id)
        ON DELETE CASCADE,
    CONSTRAINT fk_class_subjects_subject_school
        FOREIGN KEY (school_id, subject_id)
        REFERENCES subjects(school_id, id)
        ON DELETE CASCADE
);

CREATE TABLE teacher_assignments (
    id                  INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    school_id           INTEGER NOT NULL REFERENCES schools(id) ON DELETE CASCADE,
    teacher_profile_id  INTEGER NOT NULL,
    class_id            INTEGER NOT NULL,
    stream_id           INTEGER,
    subject_id          INTEGER NOT NULL,
    term_id             INTEGER NOT NULL,
